class Meme:
    def __init__(self, picture, topText, bottomText):
        self.picture = picture
        self.topText = topText
        self.bottomText = bottomText

    def print(self):
        print(self.topText)
        print(self.picture)
        print(self.bottomText)

    def toString(self):
        return self.topText + "\n" + self.picture + "\n" + self.bottomText


