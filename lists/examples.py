#Creating a list
list = ["a","b","c"]

#Getting the index 2 of list
print(list[2])

#Changing the index 2 of list
list[2] = 1

#Printing index 2 of list again
print(list[2])

#Creating a 2D list
list_of_lists = [["a",'b','c'],[1,2,3]]

#Getting index 2 of the first list in the 2D list
print(list_of_lists[0][2])

#Changing the index 2 of the first list of the 2D list
list_of_lists[0][2] = 1

#Printing index 2 of the first list of the 2D list again
print(list_of_lists[0][2])